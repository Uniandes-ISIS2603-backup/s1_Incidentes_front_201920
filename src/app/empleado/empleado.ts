/**
  * Representa un empleado.
  */
export class Empleado {
  /**
  * Id del empleado
  */
  id: number;

  /**
  * Username del empleado
  */
  username: string;

  /**
  * Password del empleado
  */
  password: string;

  /**
  * Nombre del empleado
  */
  name: string;
 
  /**
  * Numero de incidentes del empleado
  */
  numIncidentes: number;
}